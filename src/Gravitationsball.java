import GLOOP.*;
public class Gravitationsball extends Ball{

    double fallRichtung = -1;
    public Gravitationsball (double pX, double pY , double pZ, double pRadius){
        super(pX,pY,pZ,pRadius);
    }

    @Override
    public void bewegeDich() {
       kugel.verschiebe(bewegung);
        gravitation();
    }


    public void gravitation (){

     kugel.verschiebe(0,fallRichtung,0);

        if (kugel.gibY()< 1 || kugel.gibY() > 400 ){
          fallRichtung =  fallRichtung * (-1);
        }

    }
}










































































































































































































